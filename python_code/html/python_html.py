# HTML(HyperText Markup Language/超级文本标记语言)
# “超文本”就是指页面内可以包含图片、链接，甚至音乐、程序等非文字元素。
# 超文本标记语言的结构包括“头”部分（英语：Head）、和“主体”部分（英语：Body）
# 其中“头”部提供关于网页的信息，“主体”部分提供网页的具体内容。
# html语言对大小写不敏感，即不区分大小写。

#<!DOCTYPE html>来表明当前为html5的规范
#<meta charset="utf-8">设置编码格式为utf-8
#<p></p>用<p>来分割段落,p代表paragraph段落(段落之间默认有空行)
#<br>用来换行，br代表break
#<h1></h1>用来标记标题，后面的数字越小，级别越高，总共有h1-h6 6个级，标题默认会单独占据一行
#<wbr></wbr>wbr代表word-break指定内容是可以在换行时被浏览器分开的,比如太长的英文单词
#<hgroup></hgroup>用来把所有级别的标题包裹起来进行组合
#<del></del>和<ins></ins>标签配合使用，来描述文档中的更新和修正。

