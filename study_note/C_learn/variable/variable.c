/*整理总结C语言中的变量*/

/*
1.不同的编译器对变量分配的内存空间是不同的
例如，Turbo C 2.0和Turbo C++ 3.0为整型分配2个字节，而Vistual C++ 分配4个字节
2.整型包括：基本整型int、短整型short int(或short)、长整型long int(或long)
以上3类都可以加上unsigned 表示为无符号数，加上signed 表示为有符号数，如果不写，默认为有符号数
在一个整常量后面加上字母u(或U)则认为是unsigned int型，如12345u
在一个整常量后面加上字母l(或L)则认为是long int型，123l
3.浮点型包括：单精度float、双精度double float、长精度long double
一个浮点型数据一般在内存中占4个字节，浮点型数据按照指数形式存储，小数和指数是分开存储的
小数占的位数越多，数字的精度就越高，指数占的位数越多，能表示的数值范围就越大
在4个字节中小数和指数各占多少位没有具体规定，一般由不同的编译器自定
4.浮点型数据存在舍入误差，应当避免将一个很大的数与一个很小的数相加减，否则小数可能会丢失
5.字符型变量用来存放字符常量，用关键字char定义，只能存放一个字符
编译系统一般都是用一个字节存放一个字符，即一个字符变量在内存中占一个字节
字符型数据在存储时实际是将其ASC码放入存储中，相当于存储整数，因此可以进行算数运算
6.字符串常量是一对双引号括起来的字符序列，在C语言中没有专门的字符串变量，需要用字符型数组来存放字符串
字符常量'a'和字符串常量"a"是不同的，C语言中每个字符串常量结尾都以'\0'作为结束标志
*/

