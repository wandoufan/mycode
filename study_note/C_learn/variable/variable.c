void test1()
{
    /*整型变量*/
    int a;
    a = 1; //可以先声明变量，再赋值
    int b= 2; //可以在声明变量时直接赋值
    int a1 = 1, b1 = 2, c1 = 3; //声明可以给所有变量赋值
    int a2, b2, c2 = 3; //声明时可以给部分变量赋值
    int a3, b3, c3;
    a3 = b3 = c3 = 1; //可以在赋值时同时赋值
    //注意：int a = b = c = 3; 是错误的写法
}


void test2()
{
    /*浮点型变量*/
    float f;
    f = 3.14;
    printf("%f\n", f);
}


void test3()
{
    /*字符型变量*/
    char c1, c2;
    c1 = 'a'; //注意: c1 = "a"; 是错误的，不能把一个字符串常量赋给一个字符变量
    c2 = 'B';
    printf("%c\n", c1);
    printf("%d\n", c2);
    c2 = c2 + 32; //小写字母比其大写字母的ASC码值大32
    printf("%c\n", c2);
    printf("%d\n", c2);

}


void test4()
{
    /*字符串常量*/
    printf("hello world\n");
    printf("%s\n", "hello world");
}


void test5()
{
    /*自增自减运算符*/
    //注意：只能对整型变量使用自增或自减符，不能用于常量或表达式
    //5 ++ 和(a + b) ++ 都是错误写法
    int i, j;
    i = j = 2;
    printf("%d, %d\n", i ++, j --); //先使用变量，再改变变量值
    printf("%d, %d\n", i, j);
    printf("%d, %d\n", ++ i, -- j); //先改变变量值，再使用变量
    printf("%d, %d\n", i, j);
}


int main()
{
    test1();
    test2();
    test3();
    test4();
    test5();
}