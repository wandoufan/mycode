# include <stdio.h>

/*整理总结C++中命名空间的用法*/

/*
1. C++使用名称空间(namespace)将代码组成逻辑组，从而避免了名称冲突(collision)  
而C不使用名称空间，C语言中所有符号名称都定义在一个全局命名空间中  
当C代码包含多个库，尤其库是不同人编写时就很可能发生名称冲突  

2. namespace是C++中的关键字，用来定义一个命名空间，语法格式为：
namespace space_name{
	// 各种代码声明
	variables;
	functions;
	classes;
}

3. 使用命名空间的声明：
using namespace space_name;
*/




